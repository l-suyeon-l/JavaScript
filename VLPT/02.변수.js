// 변수(let) : 바뀔 수 있는 값
// ※ 한 번 선언했으면 똑같은 이름으로 선언하지 못함
let value = 1;
console.log(value);

// let value = 2; 불가
value = 2;
console.log(value);



// 상수(const) : 바뀌지 않는 값
const a = 1;
// a = 2; Error: "a" is read-only



